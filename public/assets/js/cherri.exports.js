function normalizeUser(user) {
	if (!user || typeof user !== "object") return null;
	return {
		id: String(user.id || ""),
		username: String(user.username || ""),
		display: String(user.display || user.username || ""),
		badges: Array.isArray(user.badges) ? user.badges : ["user"],
		pfp: String(user.pfp || "/assets/img/fav.png"),
		is_admin: Boolean(user.is_admin),
		trusted: user.trusted === true,
	};
}

export function escapeChars(input) {
	return String(input || "").replace(/<\/?[^>]+(>|$)/g, "");
}

export async function apiRequest(path, options = {}) {
	const requestOptions = {
		method: options.method || "GET",
		headers: {
			"content-type": "application/json",
			...(options.headers || {}),
		},
		credentials: "same-origin",
	};

	if (options.body !== undefined) {
		requestOptions.body = JSON.stringify(options.body);
	} else {
		delete requestOptions.headers["content-type"];
	}

	let response;
	try {
		response = await fetch(path, requestOptions);
	} catch (error) {
		const message =
			error && error.message
				? error.message
				: "Network error while contacting the server";
		throw new Error(message, { cause: error });
	}
	const payload = await response.json().catch(() => null);

	if (!response.ok) {
		const message =
			payload && payload.error ? payload.error : "Request failed";
		throw new Error(message);
	}

	return payload;
}

export async function getData() {
	try {
		const payload = await apiRequest("/api/auth/me");
		return normalizeUser(payload.user);
	} catch {
		return null;
	}
}

export async function isSignedIn() {
	const user = await getData();
	return Boolean(user);
}

export async function getMetaData() {
	const user = await getData();
	if (!user) return null;
	return {
		display: user.display,
		badges: user.badges,
		pfp: user.pfp,
	};
}

